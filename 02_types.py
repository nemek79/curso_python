print(10,type(10))
print(-6,type(-6))
print("float:")
print(3.14,type(3.14))
print(-2.71828,type(-2.71828))
print(1e10,type(1e10))
print("complex:")
print(1 + 2j, type(1 + 2j))
print("str:")
print("Hello, World!", type("Hello, World!"))
print("bool:")
print(True, type(True))
print(False, type(False))
print("list:")
print([1, 2, 3], type([1, 2, 3]))
print(["apple", "banana", "cherry"], type(["apple", "banana", "cherry"]))
print("tuple:")
print((1, 2, 3), type((1, 2, 3)))
print(("apple", "banana", "cherry"), type(("apple", "banana", "cherry")))
print("set:")
print({1, 2, 3}, type({1, 2, 3}))
print({"apple", "banana", "cherry"}, type({"apple", "banana", "cherry"}))
print("dict:")
print({"name": "Alice", "age": 30}, type({"name": "Alice", "age": 30}))
print({"name": "Alice", "age": 30, "city": "New York"}, type({"name": "Alice", "age": 30, "city": "New York"}))
print("None:")
print(None, type(None))
